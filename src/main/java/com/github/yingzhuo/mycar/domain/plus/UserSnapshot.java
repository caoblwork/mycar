package com.github.yingzhuo.mycar.domain.plus;

import java.io.Serializable;

public class UserSnapshot implements Serializable {

	private static final long serialVersionUID = -565550687674117498L;

	private Integer id;
	private String nickname;
	private String email;
	private Gender gender;
	
	public UserSnapshot() {
		super();
	}

	public UserSnapshot(Integer id, String nickname, String email, Gender gender) {
		super();
		this.id = id;
		this.nickname = nickname;
		this.email = email;
		this.gender = gender;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public Gender getGender() {
		return gender;
	}

	public void setGender(Gender gender) {
		this.gender = gender;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

}
